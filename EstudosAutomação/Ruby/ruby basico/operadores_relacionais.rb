puts "2 é maior que 1 #{2 < 1}" #imprime FALSE

puts "2 é maior que 1 #{2 == 2}" #imprime TRUE (é igual?)

puts "2 é maior que 1 #{2 => 1}" #imprime TRUE

puts "2 é maior que 1 #{2 <= 1}" #imprime FALSE

puts "2 é maior que 1 #{2 != 1}" #imprime TRUE (é diferente um do outro?)

puts "2 é maior que 1 #{2 <=> 3}" #imprime -1 (QUANDO O ELEMENTO DA DIREITA É MAIOR QUE A ESQUERDA)
puts "2 é maior que 1 #{2 <=> 2}" #imprime 0 (QUANDO OS ELEMENTOS SAO IGUAIS)
puts "2 é maior que 1 #{2 <=> 1}" #imprime 1 (QUANDO O ELEMENTO DA ESQUERDA É MAIOR QUE DA DIREITA)